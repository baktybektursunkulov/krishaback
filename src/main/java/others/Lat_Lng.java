package others;

public class Lat_Lng {

  private Double lat;
  private Double lng;

  public Double getLat() {
    return lat;
  }

  public void setLat(Double lat) {
    this.lat = lat;
  }

  public Double getLng() {
    return lng;
  }

  public void setLng(Double lng) {
    this.lng = lng;
  }

  public Lat_Lng(Double lat_, Double lng_) {
    this.lat = lat_;
    this.lng = lng_;
  }

}
